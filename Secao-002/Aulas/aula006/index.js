let nome = "João";
var nome2 = "Maria"; // ANTIGO NÃO É UTILIZADO MAIS
let nome3 = "Eduardo";

console.log(nome, "nasceu em 1984");
console.log("Em 2000", nome, "conheceu", nome2);
console.log(nome, "casou-se com", nome2, "em 2012");
console.log(nome2, "teve 1 filho com", nome, "em 2015");
console.log("O filho de", nome, "se chama", nome3);

/*  
Regras para criação de Variáveis:

° Você não pode criar variáveis com palavras reservadas da linguagem
   let, if, console, etc

° Variáveis precisam ter nomes significativos
    nomeCliente, nomeProduto, nomeEmpresa

° Nomes não podem começar com número

° Nomes não podem conter espaços ou traços
    nome Cliente, nome-Cliente

° Em JavaScript utilizamos camelCase
  nomeCompleto, luzAcesa, tipoProduto, nomeCompletoDoCliente

° JavaScript é Case-sensitive maiúsculas e minusculas são diferentes

° Não podemos redeclarar variáveis com let

° NÃO UTILIZE VAR, UTILIZE LET
*/
